    function task3() {
    // Declare and initialize a strings array
    let studentNames = ["Bob","Alice",  "Charlie", "David"];
    
    // Accessing elements in the array
    console.log(studentNames[0]); // Output: "Bob"
    console.log(studentNames[1]); // Output: "Alice"
    
    // Adding a new element to the array
    studentNames.push("Okentub");
    
    // Display the updated array
    console.log(studentNames); // Output: ["Alice", "Bob", "Charlie", "David", "Okentub"]
    
    }
    
